                     <div class="underdiv"><h1>How to make a Welsh rarebit</h1> Ingredients 225g mature Welsh cheddar 25g butter, melted 1 tablespoon Worcestershire sauce (or vegetarian equivalent) 1 tablespoon mustard 1 tablespoon plain flour freshly ground black pepper 4 tablespoons Welsh beer 1 tablespoon laverbread 4 thick slices of bread </div>
                     <div class="underdiv"><h1>Method</h1>01 Grate the cheese and put it in a saucepan along with the butter, Worcestershire sauce, mustard, flour and pepper. Mix well and put over a gentle heat. Gradually add the beer to moisten but do not make the mixture too wet. Stir until melted and when it has reached a thick paste remove from the heat and leave to cool a little.  02 Meanwhile toast the bread on one side only then spread the laverbread over the untoasted side before spreading the cheese mixture. Grill gently until the topping is cooked through and well browned.  03 The mixture can be made and kept in the refrigerator for several days if required.   </div>
